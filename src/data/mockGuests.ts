const now = dateOnly(new Date());

function portalFields(name: string, showName: string, overrides: Partial<Guest> = {}): Pick<Guest,
  | 'hostName'
  | 'hostEmail'
  | 'producerName'
  | 'producerEmail'
  | 'interviewLocationName'
  | 'interviewAddress'
  | 'parkingNotes'
  | 'arrivalInstructions'
  | 'recordingPrepNotes'
  | 'calendarLink'
  | 'recordingLink'
  | 'episodeLink'
  | 'spotifyLink'
  | 'appleLink'
  | 'youtubeLink'
  | 'pressKitLink'
  | 'releaseFormLink'
  | 'clipLinks'
  | 'guestPortalSlug'
  | 'guestPortalEnabled'
> {
  const slug = slugify(`${showName}-${name}`);
  return {
    hostName: 'Jordan Miles',
    hostEmail: 'host@example.com',
    producerName: 'Alex Producer',
    producerEmail: 'producer@example.com',
    interviewLocationName: 'Studio B',
    interviewAddress: '123 Main Street, Boston, MA',
    parkingNotes: 'Use the visitor spots on the right side of the building. Text the producer if the lot is full.',
    arrivalInstructions: 'Arrive 15 minutes early. Bring water, avoid noisy jewelry, and check in at the front desk.',
    recordingPrepNotes: 'We keep it conversational. Review the topic outline, think of 2-3 stories with real details, and avoid scripted answers.',
    calendarLink: 'https://example.com/calendar-invite',
    recordingLink: 'https://example.com/recording-room',
    episodeLink: overrides.episodeLink || 'https://example.com/episode-link',
    spotifyLink: 'https://example.com/spotify',
    appleLink: 'https://example.com/apple',
    youtubeLink: 'https://example.com/youtube',
    pressKitLink: 'https://example.com/press-kit',
    releaseFormLink: 'https://example.com/release-form',
    clipLinks: overrides.clipLinks ?? [
      {
        id: `${slug}-clip-1`,
        title: 'Best 45-second clip',
        url: 'https://example.com/clip-1',
        platform: 'instagram',
        suggestedCaption: `Had a great conversation on ${showName}. This clip gets into the part most people skip.`,
      },
      {
        id: `${slug}-clip-2`,
        title: 'LinkedIn pull quote clip',
        url: 'https://example.com/clip-2',
        platform: 'linkedin',
        suggestedCaption: `A few practical lessons from my interview on ${showName}. Full episode link is here.`,
      },
    ],
    guestPortalSlug: slug,
    guestPortalEnabled: true,
  };
}

